	}

	/*=============================================
	BORRAR Equipo
	=============================================*/
	static public function ctrEliminarEquipo(){

		if(isset($_GET["idEquipo"])){

			$tabla ="Equipos";
			$datos = $_GET["idEquipo"];

			if($_GET["imagen"] != "" && $_GET["imagen"] != "vistas/img/Equipos/default/anonymous.png"){

				unlink($_GET["imagen"]);
				rmdir('vistas/img/Equipos/'.$_GET["codigo"]);

			}

			$respuesta = ModeloEquipos::mdlEliminarEquipo($tabla, $datos);

			if($respuesta == "ok"){

				echo'<script>

				swal({
					  type: "success",
					  title: "El Equipo ha sido borrado correctamente",
					  showConfirmButton: true,
					  confirmButtonText: "Cerrar"
					  }).then(function(result){
								if (result.value) {

								window.location = "Equipos";

								}
							})

				</script>';

			}		
		}


	}

